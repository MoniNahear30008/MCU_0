/**
    The RFC 4493, in section 2.4 describes how to handle the last message block
    to generate the final digest value. This function implements the said logic
    to generate the final authentication tag.

    @code{.unparsed}
    cmacCtx = AES_CMACGetInternalContext(aCtx)

    if (NULL == cmacCtx) || (AES_CMAC_CONTEXT_MAGIC != cmacCtx->magic) ||
        (0UL == aDataLen) || (NULL == aDataIn) || (NULL == aAuthTag)
        return BCM_ERR_INVAL_PARAMS

    uint32_t *K
    uint32_t ML[4UL] = {};
    numFullBlocks = aDataLen / 16UL;
    numBytesRem = aDataLen % 16UL;

    if 0UL == numBytesRem
        AES_CMACUpdateInternal(cmacCtx, 16UL * (numFullBlocks - 1UL), aDataIn)
        K = cmacCtx->k1
        BCM_MemCpy(ML, &aDataIn[16UL * (numFullBlocks - 1UL)], 16UL)
    else
        AES_CMACUpdateInternal(cmacCtx, 16UL * numFullBlocks, aDataIn)
        K = cmacCtx->k2
        BCM_MemCpy(ML, &aDataIn[16UL * numFullBlocks], numBytesRem)
        uint8_t *const firstPadByteAddr = ((uint8_t*)(void*)ML) + numBytesRem
        *firstPadByteAddr = 0x80U

    for (i = 0UL; i < 4UL; i++) {
        ML[i] ^= K[i]
    }

    AES_CMACUpdateInternal(cmacCtx, 16UL, (uint8_t*)(void*)ML)

    BCM_MemCpy(aAuthTag, cmacCtx->cbcMacState, 16UL)
    return BCM_ERR_OK
    @endcode

    @trace #BRCM_SWARCH_AES_CMAC_UPDATE_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
int32_t AES_CMACFinishAndTag(AES_CMACContextType *const aCtx, const uint32_t aDataLen, const uint8_t *const aDataIn, uint8_t *const aAuthTag)
{
    int32_t ret;
    AES_CMACPrivateContextType *const cmacCtx = AES_CMACGetInternalContext(aCtx);

    if ((NULL != cmacCtx) && (AES_CMAC_CONTEXT_MAGIC == cmacCtx->magic) &&
        (0UL < aDataLen) && (NULL != aDataIn) && (NULL != aAuthTag)) {
        uint32_t i;
        const uint32_t *K;
        uint32_t ML[4UL] = {};
        const uint32_t numFullBlocks = aDataLen / 16UL;
        const uint32_t numBytesRem = aDataLen % 16UL;

        if (0UL == numBytesRem) {
        /* @event      CERT INT30-C
                  @issue       Coverity infers that (16UL * (numFullBlocks - 1UL)) can wrap
                  @risk           No risk
                   @justification aCipherCfg is assigned with a definite number and validated inside AES_ValidateKeyTypeAndGetCfg function. */
        /* coverity[cert_int30_c_violation] */
            AES_CMACUpdateInternal(cmacCtx, 16UL * (numFullBlocks - 1UL), aDataIn);

            K = cmacCtx->k1;
            /* coverity[cert_int30_c_violation] */
            BCM_MemCpy(ML, &aDataIn[16UL * (numFullBlocks - 1UL)], 16UL);
        } else {
            AES_CMACUpdateInternal(cmacCtx, 16UL * numFullBlocks, aDataIn);

            K = cmacCtx->k2;
            BCM_MemCpy(ML, &aDataIn[16UL * numFullBlocks], numBytesRem);
            uint8_t *const firstPadByteAddr = ((uint8_t*)(void*)ML) + numBytesRem;
            *firstPadByteAddr = 0x80U;
        }

        for (i = 0UL; i < 4UL; i++) {
            ML[i] ^= K[i];
        }

        AES_CMACUpdateInternal(cmacCtx, 16UL, (uint8_t*)(void*)ML);

        BCM_MemCpy(aAuthTag, cmacCtx->cbcMacState, 16UL);
        ret = BCM_ERR_OK;

    } else {
        ret = BCM_ERR_INVAL_PARAMS;
    }

    return ret;
}

/** @brief Helper function to validate parameters for #AES_KBKDFCtrModeCmacPrf

    @param[in]  aCipherKey  The AES cipherkey input to the KDF construction
    @param[in]  aLabelLen   KDF label input string length in bytes
    @param[in]  aLabel      Pointer to the KDF label input
    @param[in]  aContextLen KDF context input string length in bytes
    @param[in]  aContext    Pointer to the KDF context input
    @param[in]  aDkmLen     Required length in bytes of the output derived key
                            material string
    @param[in]  aDkm        The buffer into which the output string is to be
                            written into

    Return values are documented in reverse-chronological order
    @retval #BCM_ERR_OK             DKM populated into the provided buffer
    @retval #BCM_ERR_INVAL_PARAMS   aDkmLen is 0 or is greater than
                                    #AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN
    @retval #BCM_ERR_INVAL_PARAMS   aContextLen is greater than
                                    #AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN
    @retval #BCM_ERR_INVAL_PARAMS   aLabelLen is greater than
                                    #AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN
    @retval #BCM_ERR_INVAL_PARAMS   aDkm is NULL
    @retval #BCM_ERR_INVAL_PARAMS   aContext is NULL while aContextLen is
                                    non-zero
    @retval #BCM_ERR_INVAL_PARAMS   aLabel is NULL while aLabelLen is non-zero
    @retval #BCM_ERR_INVAL_PARAMS   aCipherKey is NULL

    @code{.unparsed}
        Validate each parameter.
        If all parameters are valid
            return BCM_ERR_OK
        else
            return BCM_ERR_INVAL_PARAMS
    @endcode

    @trace #BRCM_SWARCH_AES_KBKDF_CTRMODE_CMACPRF_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
static int32_t AES_KBKDFCtrModeValidateArgs(const uint8_t *const aCipherKey,
                                            const uint32_t aLabelLen,
                                            const uint8_t *const aLabel,
                                            const uint32_t aContextLen,
                                            const uint8_t *const aContext,
                                            const uint32_t aDkmLen,
                                            uint8_t *const aDkm)
{
    int32_t ret;

    ret = BCM_ERR_OK;

    if ((NULL == aCipherKey)
        || ((0UL != aLabelLen) && (NULL == aLabel))
        || ((0UL == aContextLen) && (NULL == aContext))
        || (NULL == aDkm)) {
        ret = BCM_ERR_INVAL_PARAMS;
    }

    if (BCM_ERR_OK == ret) {
        if ((AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN < aLabelLen)
            || (AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN < aContextLen)
            || (0UL == aDkmLen)
            || (AES_KBKDF_CTRMODE_CMACPRF_MAX_STRING_LEN < aDkmLen)) {
            ret = BCM_ERR_INVAL_PARAMS;
        }
    }

    return ret;
}

/**
    @code{.unparsed}
    Validate the inputs with #AES_KBKDFCtrModeValidateArgs

    numIter = BCM_DIV_CEIL(aDkmLen, 16UL)
    AES_CMACInitContext(aKeyType, aCipherKey, &prfCtx)

    for (i = 0UL; i < numIter; i++) {
        Reset the CMAC context

        r = i
        a. Construct the string r || Label || 0x00 || Context || L lazily: 16 bytes at a time.
        b. CMAC digest the constructed 16 bytes: by either calling #AES_CMACUpdate or #AES_CMACFinishAndTag.
        c. Repeat a and b until the entire string is consumed and the cmac tag is available.

        BCM_MemCpy(aDkm + (i * 16UL), cmacTag, copyLen);
    }

    return #BCM_ERR_OK

    @endcode

    @trace #BRCM_SWARCH_AES_KBKDF_CTRMODE_CMACPRF_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
int32_t AES_KBKDFCtrModeCmacPrf(const CRYPTO_KeyType aKeyType, const uint8_t *const aCipherKey,
                                const uint32_t aLabelLen, const uint8_t *const aLabel,
                                const uint32_t aContextLen, const uint8_t *const aContext,
                                const uint32_t aDkmLen, uint8_t *const aDkm)
{
    int32_t ret;
    const AES_CipherConfigType *cipherCfg = NULL;

    ret = AES_KBKDFCtrModeValidateArgs(aCipherKey, aLabelLen, aLabel,
                                      aContextLen, aContext, aDkmLen, aDkm);
    if ((BCM_ERR_OK == ret)
        && (BCM_ERR_OK == AES_ValidateKeyTypeAndGetCfg(aKeyType, &cipherCfg))) {
        uint32_t i;

        uint8_t ctr = 0U;
        uint8_t zero = 0U;
        const uint32_t dkmLenNetOrd = CPU_NativeToBE32(aDkmLen * 8UL);
        const uint8_t *dkmLenStr = (const uint8_t*)(&dkmLenNetOrd) + 2UL;

        const uint32_t numIter = BCM_DIV_CEIL(aDkmLen, 16UL);
        const uint32_t totalPrfOutputSize = 16UL * numIter;
        /* @event      CERT INT30-C
                  @issue       Coverity infers that (16UL - (totalPrfOutputSize - aDkmLen)) can wrap
                  @risk           No risk
                  @justification aCipherCfg is assigned with a definite number and validated inside AES_ValidateKeyTypeAndGetCfg function. */
        /* coverity[cert_int30_c_violation] */
        const uint32_t lastChunkSize = 16UL - (totalPrfOutputSize - aDkmLen);

        AES_CMACContextType prfCtx;
        AES_CMACPrivateContextType *const prfIntCtx = AES_CMACGetInternalContext(&prfCtx);

        const uint32_t prfInSubstrLen[5UL] = {1UL, aLabelLen, 1UL, aContextLen, 2UL};
        const uint8_t *prfInSubstrAddr[5UL] = {&ctr, aLabel, &zero, aContext, dkmLenStr};

        const int32_t retVal = AES_CMACInitContext(aKeyType, aCipherKey, &prfCtx);
        BCM_ASSERT(BCM_ERR_OK == retVal);

        uint8_t cmacIn[16UL] = {};
        uint8_t cmacTag[16UL] = {};

        for (i = 0UL; i < numIter; i++) {
            uint32_t endCMAC;
            uint32_t prfInSubstrIdx = 0UL;
            uint32_t substrConsumedSize = 0UL;

            ctr = (uint8_t)(i + 1UL);
            BCM_MemSet(prfIntCtx->cbcMacState, 0x00, 16UL);

            do {
                uint32_t inLen = 0UL;
                do {
                    const uint32_t numBytesToConsume =
                        BCM_MIN(prfInSubstrLen[prfInSubstrIdx] - substrConsumedSize, 16UL - inLen);

                    if (0UL < numBytesToConsume) {
                        BCM_MemCpy(cmacIn + inLen,
                                   prfInSubstrAddr[prfInSubstrIdx] + substrConsumedSize,
                                   numBytesToConsume);

                        substrConsumedSize += numBytesToConsume;
                        inLen += numBytesToConsume;
                    }

                    if (substrConsumedSize == prfInSubstrLen[prfInSubstrIdx]) {
                        prfInSubstrIdx++;
                        substrConsumedSize = 0UL;
                    }

                    endCMAC = (5UL == prfInSubstrIdx) ? 1UL : 0UL;
                    if (1UL == endCMAC) {
                        break;
                    }

                } while (inLen < 16UL);

                if (0UL == endCMAC) {
                    (void) AES_CMACUpdate(&prfCtx, inLen, cmacIn);
                } else {
                    (void) AES_CMACFinishAndTag(&prfCtx, inLen, cmacIn, cmacTag);
                }
            } while (0UL == endCMAC);

            const uint32_t copyLen = ((numIter - 1UL) == i) ? lastChunkSize : 16UL;
            BCM_MemCpy(aDkm + (i * 16UL), cmacTag, copyLen);
        }

        ret = BCM_ERR_OK;
    }

    return ret;
}

/**
    This implementation replicates the "index based" sequence presened in the RFC 3394, section 2.2.1.
    The initial value A0 is fixed to the bit-string: A6A6A6A6A6A6A6A6.

    The text in the specification is reproduced below for reference.

    Inputs:  Plaintext, n 64-bit values {P1, P2, ..., Pn}, and
             Key, K (the KEK).
    Outputs: Ciphertext, (n+1) 64-bit values {C0, C1, ..., Cn}.

    1) Initialize variables.

       Set A = IV, an initial value (see 2.2.3)
       For i = 1 to n
           R[i] = P[i]

    2) Calculate intermediate values.

       For j = 0 to 5
           For i=1 to n
               B = AES(K, A | R[i])
               A = MSB(64, B) ^ t where t = (n*j)+i
               R[i] = LSB(64, B)

    3) Output the results.

       Set C[0] = A
       For i = 1 to n
           C[i] = R[i]

    @trace #BRCM_SWARCH_AES_KEY_WRAP_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
int32_t AES_KeyWrap(const CRYPTO_KeyType aKeyType, const uint8_t *const aCipherKey,
                    const uint32_t aPlainTextLen, const uint8_t *const aPlainText,
                    uint8_t *const aCipherText)
{
    int32_t ret;
    const AES_CipherConfigType *cipherCfg = NULL;

    if ((NULL != aCipherKey) &&
        (BCM_ERR_OK == AES_ValidateKeyTypeAndGetCfg(aKeyType, &cipherCfg)) &&
        (0UL < aPlainTextLen) && (0UL == (aPlainTextLen % 8UL)) && (NULL != aPlainText) &&
        (NULL != aCipherText)) {
        uint32_t i, j;
        uint32_t aesBufs[2UL][4UL];
        uint32_t aesInpBufIdx = 0UL;
        const uint32_t N = aPlainTextLen / 8UL;
        const uint64_t iv = 0xA6A6A6A6A6A6A6A6ULL;

        AES_RoundKeysType roundKeys;
        AES_ComputeEncryptRoundKeys(cipherCfg, aCipherKey, &roundKeys);

        BCM_MemCpy(aCipherText + 8UL, aPlainText, aPlainTextLen);

        BCM_MemCpy(&aesBufs[0UL][0UL], &iv, 8UL);
        for (j = 0UL; j < 6UL; j++) {
            for (i = 0UL; i < N; i++) {
                const uint32_t T = ((N * j) + (i + 1UL));
                const uint32_t TNetOrd = CPU_NativeToBE32(T);
                const uint32_t aesOutBufIdx = aesInpBufIdx ^ 0x1UL;

                BCM_MemCpy(&aesBufs[aesInpBufIdx][2UL], &aCipherText[8UL * (i + 1UL)], 8UL);
                AES_EncryptInternal(cipherCfg, &roundKeys, &aesBufs[aesInpBufIdx][0UL],
                                    &aesBufs[aesOutBufIdx][0UL]);
                BCM_MemCpy(&aCipherText[8UL * (i + 1UL)], &aesBufs[aesOutBufIdx][2UL], 8UL);

                aesBufs[aesOutBufIdx][1UL] ^= TNetOrd;
                aesInpBufIdx = aesOutBufIdx;
            }
        }

        BCM_MemCpy(aCipherText, &aesBufs[aesInpBufIdx][0UL], 8UL);
        ret = BCM_ERR_OK;

    } else {
        ret = BCM_ERR_INVAL_PARAMS;
    }

    return ret;
}

/**
    This implementation replicates the "index based" sequence presened in the RFC 3394, section 2.2.2.
    The ICV is fixed to the bit-string: A6A6A6A6A6A6A6A6.

    The text in the specification is reproduced below for reference.

    Inputs:  Ciphertext, (n+1) 64-bit values {C0, C1, ..., Cn}, and
             Key, K (the KEK).
    Outputs: Plaintext, n 64-bit values {P0, P1, K, Pn}.


    1) Initialize variables.

       Set A = C[0]
       For i = 1 to n
           R[i] = C[i]

    2) Compute intermediate values.

       For j = 5 to 0
           For i = n to 1
               B = AES-1(K, (A ^ t) | R[i]) where t = n*j+i
               A = MSB(64, B)
               R[i] = LSB(64, B)

    3) Output results.

    If A is an appropriate initial value (see 2.2.3),
    Then
       For i = 1 to n
           P[i] = R[i]
    Else
       Return an error

    @trace #BRCM_SWARCH_AES_KEY_UNWRAP_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
int32_t AES_KeyUnwrap(const CRYPTO_KeyType aKeyType, const uint8_t *const aCipherKey,
                      const uint32_t aCipherTextLen, const uint8_t *const aCipherText,
                      uint8_t *const aPlainText)
{
    int32_t ret;
    const AES_CipherConfigType *cipherCfg = NULL;

    if ((NULL != aCipherKey) &&
        (BCM_ERR_OK == AES_ValidateKeyTypeAndGetCfg(aKeyType, &cipherCfg)) &&
        (8UL < aCipherTextLen) && (0UL == (aCipherTextLen % 8UL)) && (NULL != aPlainText) &&
        (NULL != aCipherText)) {

        uint32_t i, j;
        uint32_t aesBufs[2UL][4UL];
        uint32_t aesInpBufIdx = 0UL;
        const uint32_t N = (aCipherTextLen - 8UL) / 8UL;
        uint64_t icv = 0xA6A6A6A6A6A6A6A6ULL;

        AES_RoundKeysType roundKeys;
        AES_ComputeDecryptRoundKeys(cipherCfg, aCipherKey, &roundKeys);

        BCM_MemCpy(aPlainText, aCipherText + 8UL, aCipherTextLen - 8UL);

        BCM_MemCpy(&aesBufs[0UL][0UL], aCipherText, 8UL);
        for (j = 6UL; j > 0UL; j--) {
            for (i = N; i > 0UL; i--) {
                /* @event      CERT INT30-C
                          @issue       Coverity infers that ((N * (j - 1UL)) + i)) can wrap
                          @risk           No risk
                          @justification The number N is definite number */
                /* coverity[cert_int30_c_violation] */
                const uint32_t T = ((N * (j - 1UL)) + i);
                const uint32_t TNetOrd = CPU_NativeToBE32(T);
                const uint32_t aesOutBufIdx = aesInpBufIdx ^ 0x1UL;

                BCM_MemCpy(&aesBufs[aesInpBufIdx][2UL], &aPlainText[8UL * (i - 1UL)], 8UL);
                aesBufs[aesInpBufIdx][1UL] ^= TNetOrd;

                AES_DecryptInternal(cipherCfg, &roundKeys, &aesBufs[aesInpBufIdx][0UL],
                                    &aesBufs[aesOutBufIdx][0UL]);
                BCM_MemCpy(&aPlainText[8UL * (i - 1UL)], &aesBufs[aesOutBufIdx][2UL], 8UL);

                aesInpBufIdx = aesOutBufIdx;
            }
        }

        if ( 0 == BCM_MemCmp((uint8_t*)(&aesBufs[aesInpBufIdx][0UL]), (uint8_t*)(&icv), 8UL) ) {
            ret = BCM_ERR_OK;
        } else {
            ret = BCM_ERR_AUTH_FAILED;
        }

    } else {
        ret = BCM_ERR_INVAL_PARAMS;
    }

    return ret;
}

