/**
    @{
    @brief Helper macros to extract individual bytes from a given word

    @trace #BRCM_SWARCH_AES_ECB_ENCRYPT_BLOCK_PROC
    @trace #BRCM_SWARCH_AES_ECB_DECRYPT_BLOCK_PROC
    @trace #BRCM_SWARCH_AES_GCM_INIT_CONTEXT_PROC
    @trace #BRCM_SWARCH_AES_GCM_CRYPT_UPDATE_PROC
    @trace #BRCM_SWARCH_AES_GCM_FINISH_AND_TAG_PROC
    @trace #BRCM_SWARCH_AES_GCM_FINISH_AND_AUTH_PROC
    @trace #BRCM_SWREQ_AES_SW_ROUTINES
*/
#define AES_EXTRACT_BYTE0(val)  ((val) & 0xFFUL)
#define AES_EXTRACT_BYTE1(val)  (((val) >> 8UL) & 0xFFUL)
#define AES_EXTRACT_BYTE2(val)  (((val) >> 16UL) & 0xFFUL)
#define AES_EXTRACT_BYTE3(val)  ((val) >> 24UL)
/** @} */

