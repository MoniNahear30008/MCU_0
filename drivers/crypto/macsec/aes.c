int32_t AES_GCMAADFinish(AES_GCMContextType *const aCtx, const uint32_t aAADLen, const uint8_t *const aAAD)
{
    int32_t ret;
    AES_GCMPrivateContextType *const gcmCtx = AES_GCMGetInternalContext(aCtx);

    ret = AES_GCMAADValidateArgs(gcmCtx, aAADLen, aAAD, true);
    if (BCM_ERR_OK == ret) {
        uint32_t i;
        const uint32_t numBlocksWhole = aAADLen / 16UL;
        const uint32_t numBytesRem = aAADLen % 16UL;

        /* Process the whole blocks first */
        for (i = 0UL; i < numBlocksWhole; i++) {
            const uint8_t *const authBlockPtr = aAAD + (16UL * i);

#if defined(AES_CPU_SUPPORTS_UNALIGNED_ACCESS)
            const uint32_t *const authBlock = (const uint32_t*)(const void*) authBlockPtr;
#else
            uint32_t authTextBuf[4UL];
            const uint32_t *const authBlock = authTextBuf;
            BCM_MemCpy(authTextBuf, authBlockPtr, 16UL);
#endif

            AES_GMACUpdate(&gcmCtx->gmacCtx, authBlock);
        }

        /* Process the partial block with zero padding to complete the block  */
        if (numBytesRem > 0UL) {
            uint32_t authBlock[4UL] = {0UL, 0UL, 0UL, 0UL};
            const uint8_t *const authBlockPtr = aAAD + (16UL * numBlocksWhole);

            BCM_MemCpy(authBlock, authBlockPtr, numBytesRem);
            AES_GMACUpdate(&gcmCtx->gmacCtx, authBlock);
        }
        /* @event      CERT INT30-C
            @issue       Coverity infers that (gcmCtx->aadProcessedLen += aAADLen) can wrap
            @risk           No risk
            @justification gcmCtx->aadProcessedLen is initialized to 0 in AES_GCMInitContext function. */
        /* coverity[cert_int30_c_violation] */
        gcmCtx->aadProcessedLen += aAADLen;
        gcmCtx->state = AES_GCM_CONTEXT_STATE_AAD_DONE;
    }

    return ret;
}

