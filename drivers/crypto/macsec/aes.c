int32_t AES_GCMInitContext(const CRYPTO_KeyType aKeyType, const AES_GCMOpModeType aOpMode, const uint8_t *const aCipherKey, const uint8_t *const aIV, AES_GCMContextType *const aCtx)
{
    int32_t ret;
    const AES_CipherConfigType *cipherCfg = NULL;
    AES_GCMPrivateContextType *const gcmCtx = AES_GCMGetInternalContext(aCtx);

    if ((NULL != aCipherKey) &&
        (NULL != aIV) &&
        (NULL != gcmCtx) &&
        ((AES_GCM_OP_MODE_ENCRYPT_AND_TAG == aOpMode) || (AES_GCM_OP_MODE_DECRYPT_AND_AUTH == aOpMode)) &&
        (BCM_ERR_OK == AES_ValidateKeyTypeAndGetCfg(aKeyType, &cipherCfg))) {

        gcmCtx->magic = AES_GCM_CONTEXT_MAGIC;
        gcmCtx->state = AES_GCM_CONTEXT_STATE_AAD_PROCESSING;
        gcmCtx->opMode = aOpMode;
        gcmCtx->cipherCfg = *cipherCfg;

        /* Initialize the AES encryption round keys */
        AES_ComputeEncryptRoundKeys(cipherCfg, aCipherKey, &gcmCtx->roundKeys);

        /* Initialize the GCTR and GMAC states */
        AES_GCTRInitContext(aIV, &gcmCtx->gctrCtx);
        AES_GMACInitContext(cipherCfg, &gcmCtx->roundKeys, &gcmCtx->gmacCtx);

        gcmCtx->aadProcessedLen = 0UL;
        gcmCtx->cryptProcessedLen = 0UL;

        ret = BCM_ERR_OK;

    } else {
        ret = BCM_ERR_INVAL_PARAMS;
    }

    return ret;
}

